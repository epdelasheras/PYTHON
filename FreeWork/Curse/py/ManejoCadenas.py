
cadena = "CURSO DE PYTHON"
print(cadena.lower()) #Convierte los caracteres en minusculas
print(cadena.upper()) #Convierte los caracteres en mayusculas
cadena = "cuRsO De Python"
print(cadena.swapcase()) #lo que es mayuscula pasa a ser minuscula y al reves
print(cadena.count("o")) #cuenta cuantas "o" hay en la cadena
print(cadena.replace("Python","Java")) #Reemplaza una cadena por otra.
print(cadena.capitalize()) #Convierte el primer caracter en mayuscula y en minusculas el resto
print(cadena.find("De")) #Cuenta hacia atras cuentos caracteres ascii hay desde "De" hasta el inicio de la candena
